<div id="lebenslauf">    
    <p class="second">
      Boris Nielsen wurde 1977 in Kiel an der Ostsee geboren. Sein Studium der Geisteswissenschaften brach er 2003 erfolgreich
      ab, um in Weimar Jazz/Kontrabass zu studieren.  2009 erwarb er an der Hochschule für Musik Weimar sein Diplom und zog
      nach Berlin.<br><br>
      Seit seiner Studienzeit führten ihn zahlreiche Konzertreisen u.a. nach China, Afrika, Japan, die USA, Russland und
      Europa, jedoch selten in seine Heimatstadt Kiel. Er veröffentlichte Alben u.a. mit FEINDREHSTAR und KÄPTN PENG & DIE
      TENTAKEL VON DELPHI. 2012 gründete er mit den Mitgliedern von Käptn Peng das Plattenlabel Kreismusik. Aktuell arbeitet
      er als Kontrabassist und manchmal auch als Pianist mit KÄPTN PENG, EMMA KOLLMORGEN (Kompakt), SUKINI (Universal), 
      VERNESA BERBO (Maximm Gorki/Thalia), CHRISTIAN ROTTLER (Pudelprodukte) und ab und zu auch bei GISBERT ZU KNYPHAUSEN und KAI SCHUHMACHER.
    </p>
    <p class="second">
      Er komponiert zusammen mit den Tentakeln von Delphi und anderen KollegInnen Theater- und Filmmusiken z.B. <i>Der Auftrag</i>  am Staatstheater Hannover mit Gastauftritten an der
      Volksbühne und am DT/Berlin, in Minsk, Wien, Marl, München. Regie: Jürgen Kuttner/Tom Kühnel.<br> 
      Kinofilm <i>Alki Alki</i> (2015), Regie: Axel Ranisch.<br> 
      Kinofilm  <i>Wer hat eigentlich die Liebe erfunden</i> (2018), Regie: Kerstin Polte.<br>
      Kinofilm <i>Die Känguru-Verschwörung</i> (2022), Regie: Marc-Uwe Kling.<br>
      Abends komponiert Boris sehr ruhige (und manchmal auch sehr laute) Musik und wird eventuell möglicherweise demnächst ein Klavieralbum veröffentlichen.
      <br>Ausserdem programmiert er in seiner Freizeit und hat zb diese Seite mit PHP und Javascript gebaut.
      Falls ihr Kontakt mit ihm aufnehmen wollt, Boris freut sich über jede  <a href="mailto:mail@borisnielsen.com" style="font-style:normal;">mail</a>!
    </p>
